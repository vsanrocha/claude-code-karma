"""
Compaction detection for Claude Code sessions.

Detects TRUE context compaction events by looking for CompactBoundaryMessage
entries (type: "system", subtype: "compact_boundary").

IMPORTANT: SessionTitleMessage (type: "summary") are NOT compaction events!
They are session titles/naming generated by Claude Code. This was a common
source of confusion - see Claude Code issue #2597.

Compaction Events:
- Occur when /compact is run (manually or automatically)
- Marked by type: "system", subtype: "compact_boundary"
- Include compactMetadata with trigger ("auto"/"manual") and preTokens

Session Titles:
- Generated by Claude Code to name sessions
- Marked by type: "summary"
- Include leafUuid pointing to a message (usually in same session)
- NOT indicators of compaction!
"""

from datetime import datetime
from typing import TYPE_CHECKING, List, Optional, Tuple

if TYPE_CHECKING:
    from .message import CompactBoundaryMessage, Message


class CompactionDetector:
    """
    Detects true context compaction events in Claude Code sessions.

    Usage:
        detector = CompactionDetector()
        for msg in session.iter_messages():
            detector.process(msg)

        is_compacted = detector.was_compacted
        count = detector.compaction_count
        compaction_events = detector.compaction_events

    The detector tracks:
    - CompactBoundaryMessage entries (true compaction)
    - SessionTitleMessage entries before conversation (project context)
    - Session title after conversation (session naming, NOT compaction)
    """

    def __init__(self):
        self.conversation_started: bool = False
        self.message_index: int = 0
        self.total_message_count: int = 0

        # True compaction events (CompactBoundaryMessage)
        self.compaction_events: List[Tuple[int, datetime, "CompactBoundaryMessage"]] = []

        # Session titles (SessionTitleMessage)
        # Before conversation = project context from previous sessions
        # After conversation = session naming (not compaction)
        self.project_context_summaries: List[str] = []
        self.project_context_leaf_uuids: List[str] = []
        self.session_titles: List[str] = []

        # Track last assistant stop_reason for compatibility
        self.last_assistant_stop_reason: Optional[str] = None

    def process(self, msg: "Message") -> None:
        """
        Process a message to update compaction state.

        Call this for each message in session order.
        """
        from .message import (
            AssistantMessage,
            CompactBoundaryMessage,
            SessionTitleMessage,
            UserMessage,
        )

        # Track conversation start
        if isinstance(msg, (UserMessage, AssistantMessage)):
            self.conversation_started = True

        # Track assistant stop_reason
        if isinstance(msg, AssistantMessage):
            stop_reason = getattr(msg, "stop_reason", None)
            if stop_reason:
                self.last_assistant_stop_reason = stop_reason

        # Handle CompactBoundaryMessage - TRUE compaction events
        if isinstance(msg, CompactBoundaryMessage):
            self.compaction_events.append((self.message_index, msg.timestamp, msg))

        # Handle SessionTitleMessage - session titles, NOT compaction
        if isinstance(msg, SessionTitleMessage):
            if self.conversation_started:
                # Session title generated after conversation started
                # This is just naming, not compaction
                if msg.summary:
                    self.session_titles.append(msg.summary)
            else:
                # Summary before conversation = project context from previous sessions
                if msg.summary:
                    self.project_context_summaries.append(msg.summary)
                if msg.leaf_uuid:
                    self.project_context_leaf_uuids.append(msg.leaf_uuid)

        self.message_index += 1
        self.total_message_count += 1

    @property
    def was_compacted(self) -> bool:
        """
        True if session underwent real context compaction.

        This is determined by the presence of CompactBoundaryMessage entries,
        which are created when /compact is run (manually or automatically).
        """
        return len(self.compaction_events) > 0

    @property
    def compaction_count(self) -> int:
        """
        Number of compaction events in this session.

        Each CompactBoundaryMessage represents one compaction event.
        """
        return len(self.compaction_events)

    @property
    def compaction_summaries(self) -> List[str]:
        """
        Get descriptions of compaction events.

        Returns the content field from each CompactBoundaryMessage,
        along with trigger and token info if available.
        """
        summaries = []
        for _, _, msg in self.compaction_events:
            parts = [msg.content or "Conversation compacted"]
            if msg.trigger:
                parts.append(f"({msg.trigger})")
            if msg.pre_tokens:
                parts.append(f"[{msg.pre_tokens:,} tokens]")
            summaries.append(" ".join(parts))
        return summaries

    @property
    def has_project_context(self) -> bool:
        """True if session loaded context from previous sessions."""
        return len(self.project_context_summaries) > 0

    @property
    def session_end_summary(self) -> Optional[str]:
        """
        Get the session end summary, if any.

        For backward compatibility - returns the last session title
        if one exists after conversation started.
        """
        if self.session_titles:
            return self.session_titles[-1]
        return None

    def get_compaction_details(self) -> List[dict]:
        """
        Get detailed information about each compaction event.

        Returns:
            List of dicts with keys: index, timestamp, trigger, pre_tokens, content
        """
        return [
            {
                "index": idx,
                "timestamp": ts.isoformat() if ts else None,
                "trigger": msg.trigger,
                "pre_tokens": msg.pre_tokens,
                "content": msg.content,
            }
            for idx, ts, msg in self.compaction_events
        ]


# Keep old constants for backward compatibility (used in tests)
SESSION_END_PATTERNS = [
    "user exited",
    "session ended",
    "user exit",
    "exited claude",
    "session complete",
    "session terminated",
    "session closed",
]
SESSION_END_THRESHOLD = 3
